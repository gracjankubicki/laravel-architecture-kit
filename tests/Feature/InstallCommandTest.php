<?php

declare(strict_types=1);

namespace Taqie\ArchitectureKit\Tests\Feature;

use Illuminate\Filesystem\Filesystem;
use Taqie\ArchitectureKit\Architecture;
use Taqie\ArchitectureKit\Tests\TestCase;

class InstallCommandTest extends TestCase
{
    public function test_it_generates_config_guideline_and_selected_skills(): void
    {
        $this->artisan('architecture-kit:install')
            ->expectsChoice('Which architecture patterns does this project use?', [
                'thin-controllers',
                'form-requests',
                'actions',
                'data-objects',
                'api-resources',
            ], Architecture::promptOptions())
            ->expectsConfirmation('Install Architecture Kit MCP and hooks for AI agents now?', 'no')
            ->expectsConfirmation('Continue?', 'yes')
            ->assertExitCode(0);

        $files = new Filesystem();

        $config = $files->get($this->tempPath.'/config/architectures.php');
        $guideline = $files->get($this->tempPath.'/.ai/guidelines/architecture-kit.md');
        $skill = $files->get($this->tempPath.'/.ai/skills/architecture-kit-actions/SKILL.md');
        $formRequestSkill = $files->get($this->tempPath.'/.ai/skills/architecture-kit-form-requests/SKILL.md');

        $this->assertStringContainsString('Architecture::ThinControllers', $config);
        $this->assertStringContainsString('Architecture::Actions', $config);
        $this->assertStringContainsString('Generated by Laravel Architecture Kit', $guideline);
        $this->assertStringContainsString('### Actions', $guideline);
        $this->assertStringContainsString('Write flow:', $guideline);
        $this->assertStringContainsString('php artisan architecture-kit:guard --changed --strict', $guideline);
        $this->assertStringContainsString('Before building custom infrastructure, search for existing Laravel features', $guideline);
        $this->assertStringContainsString('Architecture folder purity:', $guideline);
        $this->assertStringContainsString('Architecture folders MUST stay type-pure.', $guideline);
        $this->assertStringContainsString('`app/Actions/**` contains Actions only.', $guideline);
        $this->assertStringContainsString('`app/Data/**` contains Data Objects, DTOs, and Result objects only.', $guideline);
        $this->assertStringContainsString('$request->toData()', $guideline);
        $this->assertStringNotContainsString('$request->data()', $guideline);
        $this->assertStringContainsString('name: architecture-kit-actions', $skill);
        $this->assertStringContainsString('Action folders MUST contain Actions only.', $skill);
        $this->assertStringContainsString('Do not put Data Objects, Result objects, Enums, Exceptions, API Resources, or Value Objects under `app/Actions/**`.', $skill);
        $this->assertStringContainsString("---\n\n<!-- Generated by Laravel Architecture Kit.", $skill);
        $this->assertStringContainsString('public function toData(): CreateInvoiceData', $formRequestSkill);
        $this->assertStringNotContainsString('public function data(', $formRequestSkill);
        $this->assertFileDoesNotExist($this->tempPath.'/.ai/skills/architecture-kit-value-objects/SKILL.md');
    }

    public function test_it_removes_stale_generated_skills(): void
    {
        $files = new Filesystem();
        $files->ensureDirectoryExists($this->tempPath.'/config');
        $files->put($this->tempPath.'/config/architectures.php', $this->configFor([Architecture::Actions]));
        $files->ensureDirectoryExists($this->tempPath.'/.ai/skills/architecture-kit-value-objects');
        $files->put(
            $this->tempPath.'/.ai/skills/architecture-kit-value-objects/SKILL.md',
            "---\nname: architecture-kit-value-objects\n---\n\n<!-- Generated by Laravel Architecture Kit. Do not edit manually. hash=sha256:old -->\n",
        );

        $this->artisan('architecture-kit:install')
            ->expectsChoice('Which architecture patterns does this project use?', ['actions'], Architecture::promptOptions())
            ->expectsConfirmation('Install Architecture Kit MCP and hooks for AI agents now?', 'no')
            ->expectsConfirmation('Continue?', 'yes')
            ->assertExitCode(0);

        $this->assertFileExists($this->tempPath.'/.ai/skills/architecture-kit-actions/SKILL.md');
        $this->assertDirectoryDoesNotExist($this->tempPath.'/.ai/skills/architecture-kit-value-objects');
    }

    public function test_it_installs_agent_mcp_and_hooks_in_the_same_preflight(): void
    {
        $this->artisan('architecture-kit:install')
            ->expectsChoice('Which architecture patterns does this project use?', ['actions'], Architecture::promptOptions())
            ->expectsConfirmation('Install Architecture Kit MCP and hooks for AI agents now?', 'yes')
            ->expectsChoice('Which AI agents should Architecture Kit configure?', ['codex', 'claude_code'], [
                'codex' => 'Codex',
                'claude_code' => 'Claude Code',
            ])
            ->expectsChoice('Which agent integrations should Architecture Kit install?', ['mcp', 'hooks'], [
                'mcp' => 'MCP server config',
                'hooks' => 'Guard hooks',
            ])
            ->expectsOutputToContain('create  resources config/architectures.php')
            ->expectsOutputToContain('create  mcp       .codex/config.toml')
            ->expectsOutputToContain('create  mcp       .mcp.json')
            ->expectsOutputToContain('create  hooks     .codex/hooks.json')
            ->expectsOutputToContain('create  hooks     .claude/settings.json')
            ->expectsOutputToContain('create  state     .architecture-kit/install.json')
            ->expectsConfirmation('Continue?', 'yes')
            ->assertExitCode(0);

        $files = new Filesystem();

        $this->assertFileExists($this->tempPath.'/.ai/skills/architecture-kit-actions/SKILL.md');
        $this->assertStringContainsString('[mcp_servers.architecture-kit]', $files->get($this->tempPath.'/.codex/config.toml'));
        $this->assertStringContainsString('"architecture-kit:mcp"', $files->get($this->tempPath.'/.mcp.json'));
        $this->assertStringContainsString('.architecture-kit/hooks/guard.sh', $files->get($this->tempPath.'/.codex/hooks.json'));
        $this->assertStringContainsString('.architecture-kit/hooks/guard.sh claude', $files->get($this->tempPath.'/.claude/settings.json'));
        $this->assertStringContainsString('"claude_code"', $files->get($this->tempPath.'/.architecture-kit/install.json'));
    }

    public function test_it_blocks_unmanaged_generated_targets_before_writing_config(): void
    {
        $files = new Filesystem();
