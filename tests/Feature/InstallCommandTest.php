        $this->assertSame('manual guideline', $files->get($this->tempPath.'/.ai/guidelines/architecture-kit.md'));
        $this->assertStringNotContainsString('Architecture::ThinControllers', $files->get($this->tempPath.'/config/architectures.php'));
    }

